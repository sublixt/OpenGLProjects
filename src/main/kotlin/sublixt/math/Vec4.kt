    public fun lerp(to: Vec4, speed: Float): Vec4 {
        val invspeed = 1f - speed
        return Vec4(
                (x * invspeed) + (to.x * speed),
                (y * invspeed) + (to.y * speed),
                (z * invspeed) + (to.z * speed),
                (w * invspeed) + (to.w * speed)
        )
    }

