    public fun get(index: Int): Float =
            when (index) {
                0 -> x
                1 -> y
                2 -> z
                3 -> w
                else -> throw IndexOutOfBoundsException("[Vec4](get(int)): " + index + " not in range [0, 3]")
            }

    public fun minus(): Vec4 = Vec4(-x, -y, -z, -w)

    public val magnitudeSquared: Float get() = dot(this)
    public val magnitude: Float get() = sqrt(magnitudeSquared)
    public val normal: Vec4 get() = div(magnitude)

    public fun project(onto: Vec4): Vec4 =
            onto * (dot(onto) / onto.magnitude)

    public fun dot(other: Vec4): Float = x * other.x + y * other.y + z * other.z + w * other.w

    public fun plus(scalar: Float): Vec4 = Vec4(x + scalar, y + scalar, z + scalar, w + scalar)
    public fun minus(scalar: Float): Vec4 = Vec4(x - scalar, y - scalar, z - scalar, w - scalar)
    public fun times(scalar: Float): Vec4 = Vec4(x * scalar, y * scalar, z * scalar, w * scalar)
    public fun div(scalar: Float): Vec4 = Vec4(x / scalar, y / scalar, z / scalar, w / scalar)

    public fun plus(scalar: Int): Vec4 = Vec4(x + scalar, y + scalar, z + scalar, w + scalar)
    public fun minus(scalar: Int): Vec4 = Vec4(x - scalar, y - scalar, z - scalar, w - scalar)
    public fun times(scalar: Int): Vec4 = Vec4(x * scalar, y * scalar, z * scalar, w * scalar)
    public fun div(scalar: Int): Vec4 = Vec4(x / scalar, y / scalar, z / scalar, w / scalar)

